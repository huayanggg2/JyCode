package com.example.demo.model;

public class Jydetail {
    private String dasn;
    private int suces;
    private int fails;
    private String serapi;
    private int perct;

    public String getDasn() {
        return dasn;
    }

    public void setDasn(String dasn) {
        this.dasn = dasn;
    }

    public int getSuces() {
        return suces;
    }

    public void setSuces(int suces) {
        this.suces = suces;
    }

    public int getFails() {
        return fails;
    }

    public void setFails(int fails) {
        this.fails = fails;
    }

    public String getSerapi() {
        return serapi;
    }

    public void setSerapi(String serapi) {
        this.serapi = serapi;
    }

    public int getPerct() {
        return perct;
    }

    public void setPerct(int perct) {
        this.perct = perct;
    }
}
